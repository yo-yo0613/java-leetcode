package PointerAndLinkedList.ReverseLinkedList;

// 這是 LeetCode 提供的節點定義
class ListNode {
    // 節點的值
    int val;
    // 指向下一個節點的指標
    ListNode next;
    // 建構子，不帶參數
    ListNode() {}
    // 建構子，帶有值
    ListNode(int val) { this.val = val;}
    // 建構子，帶有值和下一個節點指標
    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

